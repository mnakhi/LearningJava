package operator;

public class UseEqualityOperator {

    // ==      equal to
    // !=      not equal to
    // >       greater than
    // >=      greater than or equal to
    // <       less than
    // <=      less than or equal to

    public static void main(String[] args) {

        int number1=40;
        int number2=50;

        if (number1==number2){
            System.out.println("Number one is Equal to Number 2");
        }
        if (number1!=number2){
            System.out.println("Number one is not Equal to Number 2");
        }
        if (number1>number2){
            System.out.println("Number one is grater than Number 2");
        }
        if (number1>=number2){
            System.out.println("Number one is grater than Equal Number 2");
        }
        if (number1<number2){
            System.out.println("Number one is Less than Number 2");
        }
        if (number1<=number2){
            // int number1=40;
            // int number2=50;
            System.out.println("Number one is Less than Equal Number 2");
        }

    }

}




